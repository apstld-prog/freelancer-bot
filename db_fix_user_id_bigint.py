from sqlalchemy import create_engine, text
import os

# --------------------------------------------------
# Load DATABASE_URL from environment
# --------------------------------------------------
db_url = os.environ.get("DATABASE_URL")
if not db_url:
    print("‚ùå DATABASE_URL not found in environment.")
    exit(1)

engine = create_engine(db_url, future=True)

print("======================================================")
print("üß© FIX USER ID ‚Üí BIGINT MIGRATION TOOL (users + user)")
print("======================================================")

def fix_table(table_name):
    print(f"üîß Checking table: {table_name}")
    with engine.begin() as conn:
        info = conn.execute(text(f"""
            SELECT data_type FROM information_schema.columns
            WHERE table_name='{table_name}' AND column_name='id';
        """)).fetchone()

        if not info:
            print(f"‚ö†Ô∏è  Table '{table_name}' not found, skipping.")
            return

        col_type = info[0]
        print(f"‚ÑπÔ∏è Current 'id' column type: {col_type}")
        if col_type != "bigint":
            print(f"üß© Altering '{table_name}.id' from INTEGER to BIGINT...")
            conn.execute(text(f'ALTER TABLE "{table_name}" ALTER COLUMN id TYPE BIGINT;'))
            print(f"‚úÖ Column type of '{table_name}.id' changed to BIGINT.")
        else:
            print(f"‚úÖ '{table_name}.id' is already BIGINT.")

# Run for both possible tables
for tbl in ["users", "user"]:
    fix_table(tbl)

print("======================================================")
print("‚úÖ Migration complete ‚Äî id columns are now BIGINT.")
print("======================================================")
print("You can now rerun:")
print("   python3 init_users.py")
print("   python3 init_keywords.py")
